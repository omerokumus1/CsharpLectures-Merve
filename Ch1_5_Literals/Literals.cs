namespace Ch1_5_Literals
{
    internal class Program
    {
        public static void Main(string[] args)
        {
            // Literal: kod içerisinde direkt olarak gözüken değerlere literal denir. Literaller değişkenlere atanır.
            int i1 = 3; // 3 -> integer literal
            double d1 = 3.2; // 3.2 -> double literal
            char c1 = 'x'; // 'x' -> char literal
            string s1 = "programming"; // "programming" -> string literal
            bool b1 = false; // false -> boolean literal
        }
    }
}